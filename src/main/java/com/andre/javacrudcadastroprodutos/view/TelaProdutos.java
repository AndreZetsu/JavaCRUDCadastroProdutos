private void habilitarFormulario(boolean b) {
        bNovo.setEnabled(!b);
        bSalvar.setEnabled(b);
        bRemover.setEnabled(b);
        bCancelar.setEnabled(b);
        bBuscar.setEnabled(b);
        txtDescricao.setEnabled(b);
        txtQuantidade.setEnabled(b);
        txtPreco.setEnabled(b);
        ftfMarca.setEnabled(b);
        tpFuncionario.setEnabled(!b);

        if (!b) {
            limpaFormulario();
        }
    }

    private void limpaFormulario() {
        produtos = null;
        txtDescricao.setText("");
        txtQuantidade.setText("");
        txtPreco.setValue(0d);
        ftfMarca.setValue(null);
    }
    
    private boolean validarFormulario() {
        if (txtDescricao.getText().trim().length() < 3) {
            JOptionPane.showMessageDialog(this, "Nome inválido.", "Alerta", JOptionPane.WARNING_MESSAGE);
            txtDescricao.requestFocus();
            return false;
        }

        return true;
    }


    private void bSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSalvarActionPerformed
        if (validarFormulario()) {
            produtos.setDescricao(txtDescricao.getText().trim());
            //produtos.setQuantidade(txtQuantidade.getValue()));
            produtos.setPreco(Double.parseDouble(String.valueOf(txtPreco.getValue())));
            produtos.setMarca((Marca) ftfMarca.getValue());

            if (produtos.getCodigo() == 0) {
                try {
                    produtosControl.inserirFuncionario(produtos);
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Erro ao cadastrar o Produto.\n" + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
                    return;
                }
            } else {
                try {
                    produtosControl.alterarFuncionario(produtos);
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Erro ao alterar o Produtos.\n" + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
                    return;
                }
            }

            habilitarFormulario(false);
            carregarGrade();
        }
    }//GEN-LAST:event_bSalvarActionPerformed

    private void bRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bRemoverActionPerformed
        int opcao = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir o Produto " + produtos + "?");
        if (opcao == 0) {
            try {
                produtosControl.excluirFuncionario(produtos);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Erro ao excluir o Produto.\n" + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
                return;
            }

            habilitarFormulario(false);
            carregarGrade();
        }
    }//GEN-LAST:event_bRemoverActionPerformed

